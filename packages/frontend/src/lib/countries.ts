/**
 * Converts a 2-letter country code to its flag emoji
 * using Unicode Regional Indicator Symbols.
 */
export function getFlagEmoji(code: string): string {
  const codePoints = [...code.toUpperCase()].map(
    (char) => 0x1f1e6 + char.charCodeAt(0) - 65,
  );
  return String.fromCodePoint(...codePoints);
}

/**
 * Returns the country name for a given ISO code, or the code itself if not found.
 */
export function getCountryName(code: string): string {
  return COUNTRY_MAP[code.toUpperCase()] ?? code;
}

